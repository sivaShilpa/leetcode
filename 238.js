var productExceptSelf = function(nums) {
    let retVal = []
    
    for(let i=0; i<nums.length; i++){
        retVal.push(1)
    }
    for(let i=0; i<nums.length; i++){
        for(let j=0; j<retVal.length; j++){
            retVal
        }
    }
    return retVal   
}

   
let numbers = [-1,1,0,-3,3]
console.log(productExceptSelf(numbers))